# Simple calculator
print('Simple calculator: + - * / :\n')

num1 = int(input('Input first number: '))
num2 = int(input('Input second number: '))

print(num1, '+', num2, '=', num1 + num2)
print(num1, '-', num2, '=', num1 - num2)
print(num1, '*', num2, '=', num1 * num2)
print(num1, '/', num2, '=', num1 / num2)
